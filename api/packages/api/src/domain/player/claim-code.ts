/**
 * Generate a random claim code in the format ADJECTIVE-NOUN-NNNN.
 * Collision handling (retry on unique constraint violations) is the caller's responsibility.
 *
 * @returns A random uppercase claim code string, e.g. "CIPHER-TURING-7492"
 */
export function generateClaimCode(): string {
  const adjective = ADJECTIVES[Math.floor(Math.random() * ADJECTIVES.length)];
  const noun = NOUNS[Math.floor(Math.random() * NOUNS.length)];
  const number = Math.floor(Math.random() * 10_000)
    .toString()
    .padStart(4, "0");
  return `${adjective}-${noun}-${number}`;
}
